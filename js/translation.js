// 1. اقرأ اللغة المختارة من localStorage أو استخدم 'ar' كافتراضية
const savedLang = localStorage.getItem('preferredLanguage') || 'ar';

i18next.init({
    lng: savedLang,
    resources
}, function (err, t) {
    updateContent();
    updateLangToggleButton();
});

// 2. تحديث المحتوى حسب اللغة
function updateContent() {
    const currentLang = i18next.language;

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const attrKey = el.getAttribute('data-i18n');
        const attrMatch = attrKey.match(/^\[(.+)\](.+)$/);

        if (attrMatch) {
            const attrName = attrMatch[1];
            const key = attrMatch[2];
            el.setAttribute(attrName, i18next.t(key));
        } else {
            el.innerHTML = i18next.t(attrKey);
        }

        if (['INPUT', 'TEXTAREA'].includes(el.tagName)) {
            el.style.direction = currentLang === 'ar' ? 'rtl' : 'ltr';
            el.style.textAlign = currentLang === 'ar' ? 'right' : 'left';
        }
    });

    document.documentElement.lang = currentLang;
    document.documentElement.dir = currentLang === 'ar' ? 'rtl' : 'ltr';
    document.body.style.direction = currentLang === 'ar' ? 'rtl' : 'ltr';
    document.body.style.textAlign = currentLang === 'ar' ? 'right' : 'left';
}

// 3. زر تغيير اللغة
function updateLangToggleButton() {
    const btnMobile = document.getElementById('langToggleMobile');
    const btnDesktop = document.getElementById('langToggleDesktop');

    if (btnMobile) {
        btnMobile.textContent = i18next.language === 'ar' ? 'English' : 'عربي';
    }
    if (btnDesktop) {
        btnDesktop.textContent = i18next.language === 'ar' ? 'English' : 'عربي';
    }
}

// 4. عند الضغط على الزر، غير اللغة واحفظها في localStorage
function toggleLanguage() {
    const newLang = i18next.language === 'ar' ? 'en' : 'ar';
    i18next.changeLanguage(newLang, () => {
        localStorage.setItem('preferredLanguage', newLang); // حفظ اللغة
        updateContent();
        updateLangToggleButton();
    });
}

// 5. إسناد الحدث للزرين
const btnMobile = document.getElementById('langToggleMobile');
const btnDesktop = document.getElementById('langToggleDesktop');

if (btnMobile) btnMobile.addEventListener('click', toggleLanguage);
if (btnDesktop) btnDesktop.addEventListener('click', toggleLanguage);
