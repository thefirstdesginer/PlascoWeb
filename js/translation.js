// 4. Change language and save preference
function toggleLanguage() {
    const newLang = i18next.language === 'ar' ? 'en' : 'ar';
    i18next.changeLanguage(newLang, () => {
        localStorage.setItem('preferredLanguage', newLang);
        updateContent();
        updateLangToggleButton();
    });
}

// 5. Assign toggle event to both buttons
const btnMobile = document.getElementById('langToggleMobile');
const btnDesktop = document.getElementById('langToggleDesktop');

if (btnMobile) btnMobile.addEventListener('click', toggleLanguage);
if (btnDesktop) btnDesktop.addEventListener('click', toggleLanguage);
